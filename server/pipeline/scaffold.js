function indexHtml(projectName) {
  return [
    '<!doctype html>',
    '<html lang="en">',
    '  <head>',
    '    <meta charset="UTF-8" />',
    '    <meta name="viewport" content="width=device-width, initial-scale=1.0" />',
    `    <title>${escapeHtml(projectName)}</title>`,
    '  </head>',
    '  <body>',
    '    <div id="root"></div>',
    '    <script type="module" src="/src/main.jsx"></script>',
    '  </body>',
    '</html>',
    '',
  ].join('\n');
}

// Canonical bootstrap for the React app. Generated by the scaffold (not
// the LLM coder) so the BrowserRouter root is in exactly ONE place. This
// kills a whole class of "blank screen" bugs where the coder, having no
// signal about where the router should live, would emit <BrowserRouter>
// in both main.jsx AND App.jsx — react-router-dom then throws "you cannot
// render a <Router> inside another <Router>" and the tree fails to mount.
//
// The coder is told (in [CODE]) that BrowserRouter is provided here, so
// App.jsx must contain only <Routes>/<Route>. The evaluator's
// `frontend-renders-once` check enforces this at validation time.
//
// The ErrorBoundary wrapper turns any future render-time crash into a
// visible red panel instead of a blank page — debugger UX win for the
// human running the demo.
function mainJsx() {
  return [
    "import React from 'react';",
    "import ReactDOM from 'react-dom/client';",
    "import { BrowserRouter } from 'react-router-dom';",
    "import App from './App.jsx';",
    "import ErrorBoundary from './ErrorBoundary.jsx';",
    "import './index.css';",
    '',
    "ReactDOM.createRoot(document.getElementById('root')).render(",
    '  <React.StrictMode>',
    '    <ErrorBoundary>',
    '      <BrowserRouter>',
    '        <App />',
    '      </BrowserRouter>',
    '    </ErrorBoundary>',
    '  </React.StrictMode>',
    ');',
    '',
  ].join('\n');
}

// Top-level error boundary. Renders a visible red panel with the error
// message + stack when any descendant throws during render, instead of a
// blank page. Generated by the scaffold so projects always have it; the
// coder doesn't need to know it exists.
function errorBoundary() {
  return [
    "import React from 'react';",
    '',
    'export default class ErrorBoundary extends React.Component {',
    '  constructor(props) {',
    '    super(props);',
    '    this.state = { error: null };',
    '  }',
    '',
    '  static getDerivedStateFromError(error) {',
    '    return { error };',
    '  }',
    '',
    '  componentDidCatch(error, info) {',
    "    console.error('[ErrorBoundary] caught:', error, info);",
    '  }',
    '',
    '  render() {',
    '    if (this.state.error) {',
    '      return (',
    '        <div',
    '          role="alert"',
    "          style={{",
    "            margin: '1rem',",
    "            padding: '1rem',",
    "            border: '2px solid #dc2626',",
    "            borderRadius: '0.5rem',",
    "            background: '#fef2f2',",
    "            color: '#7f1d1d',",
    "            fontFamily: 'ui-monospace, SFMono-Regular, Menlo, monospace',",
    "            whiteSpace: 'pre-wrap',",
    '          }}',
    '        >',
    "          <h2 style={{ marginTop: 0, fontFamily: 'inherit' }}>",
    '            Something went wrong while rendering this page.',
    '          </h2>',
    '          <p>',
    '            <strong>{String(this.state.error?.message || this.state.error)}</strong>',
    '          </p>',
    '          {this.state.error?.stack && (',
    "            <pre style={{ fontSize: '0.75rem', overflow: 'auto' }}>",
    '              {this.state.error.stack}',
    '            </pre>',
    '          )}',
    '          <p>',
    '            <em>',
    '              This panel is rendered by a scaffolded ErrorBoundary. The',
    "              demo's React tree threw during render — check the browser",
    '              console for the full stack trace.',
    '            </em>',
    '          </p>',
    '        </div>',
    '      );',
    '    }',
    '    return this.props.children;',
    '  }',
    '}',
    '',
  ].join('\n');
}

function viteConfig(backendPort) {
  return [
    "import { defineConfig } from 'vite';",
    "import react from '@vitejs/plugin-react';",
    "import tailwindcss from '@tailwindcss/vite';",
    '',
    '// Vite dev server proxies /api to the backend so the frontend can',
    "// call `fetch('/api/...')` literally — no VITE_API_URL juggling, no",
    '// CORS preflights. Production deployments would replace this with a',
    '// reverse-proxy or by serving the built client from the backend.',
    'export default defineConfig({',
    '  plugins: [react(), tailwindcss()],',
    '  server: {',
    '    proxy: {',
    "      '/api': {",
    `        target: 'http://localhost:${backendPort}',`,
    '        changeOrigin: true,',
    '      },',
    '    },',
    '  },',
    '});',
    '',
  ].join('\n');
}

function clientPackageJson(projectName) {
  return (
    JSON.stringify(
      {
        name: `${projectName}-client`,
        private: true,
        version: '0.0.1',
        type: 'module',
        scripts: {
          dev: 'vite',
          build: 'vite build',
          preview: 'vite preview',
        },
        dependencies: { ...CLIENT_DEPS },
        devDependencies: { ...CLIENT_DEV_DEPS },
      },
      null,
      2
    ) + '\n'
  );
}

function stackBServerPackageJson(projectName) {
  return (
    JSON.stringify(
      {
        name: `${projectName}-server`,
        private: true,
        version: '0.0.1',
        type: 'module',
        main: 'index.js',
        scripts: {
          start: 'node index.js',
          dev: 'node --watch index.js',
        },
        dependencies: { ...STACK_B_SERVER_DEPS },
      },
      null,
      2
    ) + '\n'
  );
}

function rootPackageJsonStackB(projectName) {
  return (
    JSON.stringify(
      {
        name: projectName,
        private: true,
        version: '0.0.1',
        scripts: {
          // Root install is required to bring in `concurrently` (declared
          // as a root devDependency). Without it `npm run dev` fails with
          // `concurrently: command not found` even after `install:all`.
          'install:all':
            'npm install && npm --prefix client install && npm --prefix server install',
          dev: 'concurrently -k -n server,client -c cyan,magenta "npm:dev:server" "npm:dev:client"',
          'dev:client': 'npm --prefix client run dev',
          'dev:server': 'npm --prefix server run dev',
          build: 'npm --prefix client run build',
          start: 'npm --prefix server run start',
        },
        devDependencies: { ...ROOT_DEV_DEPS },
      },
      null,
      2
    ) + '\n'
  );
}

