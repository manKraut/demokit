/**
 * Write every deterministic wrapper file for a finished project.
 *
 * @param {{
 *   sessionId: string,
 *   projectName: string,
 *   stack: 'stack-a' | 'stack-b',
 *   contract: object,
 * }} args
 * @returns {Promise<string[]>} relative paths written, in write order
 */
export async function scaffoldProject({
  sessionId,
  projectName,
  stack,
  contract,
}) {
  if (!sessionId) throw new TypeError('scaffoldProject: sessionId is required');
  if (!stack) throw new TypeError('scaffoldProject: stack is required');

  // Validate stack early — getStack throws on unknown ids, which gives
  // a clearer error than failing inside a write below.
  getStack(stack);

  const name = safeProjectName(projectName);
  const backendPort = deriveBackendPort(contract, stack);
  const written = [];

  async function write(relPath, content) {
    await writeOutputFile(sessionId, relPath, content);
    written.push(relPath);
  }

  await write('client/index.html', indexHtml(name));
  await write('client/vite.config.js', viteConfig(backendPort));
  await write('client/package.json', clientPackageJson(name));

  // React bootstrap and the error boundary are scaffolded (not LLM-written)
  // so the router root lives in exactly one place. See mainJsx() comment
  // above for the bug class this prevents.
  await write('client/src/main.jsx', mainJsx());
  await write('client/src/ErrorBoundary.jsx', errorBoundary());

  if (stack === 'stack-b') {
    await write('server/package.json', stackBServerPackageJson(name));
    await write('package.json', rootPackageJsonStackB(name));
  } else {
    await write('server/requirements.txt', STACK_A_REQUIREMENTS);
    await write('package.json', rootPackageJsonStackA(name));
  }

  await write('.env.example', envExample(contract));
  await write('.gitignore', gitignore());

  // Empty asset drop folder for the human demo runner. Materialised via a
  // README so the directory exists in git/zip output without needing a
  // .gitkeep — the README also documents the drop-files-here UX. The
  // backend seed (written by the coder) references stable URLs
  // /sample-assets/<table>-<n>.<ext> that get resolved here.
  await write(
    'client/public/sample-assets/README.md',
    sampleAssetsReadme(name)
  );

  // Empty seed-data folder for real-row imports. The coder's DB init
  // file looks here first (e.g. server/seed-data/projects.csv) and
  // falls back to lorem-ipsum placeholders if the file isn't present.
  // Materialised via a README for the same reasons as sample-assets/.
  await write('server/seed-data/README.md', seedDataReadme(name));

  return written;
}

